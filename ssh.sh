example for ssh
in order to create ssh key pass ssh-keygen command
check that key in ~/.ssh location like la -la ~/.ssh
if you want to generate key in diff algorithm just do ssh-keygen -t dsa , it can be dsa , rsa ,sha types , default is rsa
	once created add this public key in settings git hub ssh key
	once done authentic server to git@github.com /enterprise edition
